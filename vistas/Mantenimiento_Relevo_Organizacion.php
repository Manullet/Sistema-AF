<script>
    // Función para abrir el modal de edición
    function abrirModalEditar(Id_Relevo, id_ficha, id_productor, tendra_relevo, cuantos_relevos, descripcion, estado) {
    document.getElementById("Id_Relevo").value = Id_Relevo;
    document.getElementById("id_ficha").value = id_ficha;
    document.getElementById("id_productor").value = id_productor;
    // Marcar el radio button correspondiente
    if (tendra_relevo === 'S') {
        document.getElementById("tendra_relevo_S").checked = true;
    } else if (tendra_relevo === 'N') {
        document.getElementById("tendra_relevo_N").checked = true;
    }
    document.getElementById("cuantos_relevos").value = cuantos_relevos;
    document.getElementById("descripcion").value = descripcion;
    document.getElementById("estado").value = estado;
    
    $('#modalEditar').modal('show'); // Mostrar el modal de edición
}
</script>

<!-- Script para mostrar el mensaje al momento de editar un usuario-->
<script>
    $(document).ready(function() {
        $("#formularioEditar").on("submit", function(event) {
            event.preventDefault();
            $.ajax({
                url: "modelos/editar_relevo_organizacion.php",
                method: "POST",
                data: $(this).serialize(),
                success: function(response) {
                    if (response == "success") {
                        Swal.fire({
                            title: "Periodo actualizado correctamente",
                            text: "El periodo se ha actualizado correctamente.",
                            icon: "success",
                            showCancelButton: false,
                            confirmButtonText: "Cerrar"
                        }).then(function() {
                            $("#modalEditar").modal("hide");
                            location.reload(); // Recarga la página
                        });
                    } else {
                        Swal.fire({
                            title: "Error",
                            text: "Hubo un problema al actualizar el periodo.",
                            icon: "error",
                            confirmButtonText: "Cerrar"
                        }).then(function() {
                            location.reload(); // Recarga la página
                        });
                    }
                }
            });
        });
    });
</script>

<!-- Script para mostrar el mensaje al momento de eliminar un usuario-->
<script>
    $(document).ready(function() {
        $("form#deleteForm").on("submit", function(event) {
            event.preventDefault();
            var form = $(this);
            Swal.fire({
                title: "¿Estás seguro?",
                text: "Esta acción eliminará el periodo. Esta acción no se puede deshacer.",
                icon: "warning",
                showCancelButton: true,
                confirmButtonText: "Sí, eliminar",
                cancelButtonText: "Cancelar"
            }).then(function(result) {
                if (result.isConfirmed) {
                    $.ajax({
                        url: form.attr("action"),
                        method: "POST",
                        data: form.serialize(),
                        success: function(response) {
                            if (response == "success") {
                                Swal.fire({
                                    title: "Periodo eliminado correctamente",
                                    text: "El periodo se ha eliminado correctamente.",
                                    icon: "success",
                                    showCancelButton: false,
                                    confirmButtonText: "Cerrar"
                                }).then(function() {
                                    location.reload(); // Recarga la página
                                });
                            } else {
                                Swal.fire({
                                    title: "Error",
                                    text: "Hubo un problema al eliminar el periodo.",
                                    icon: "error",
                                    confirmButtonText: "Cerrar"
                                }).then(function() {
                                    location.reload(); // Recarga la página
                                });
                            }
                        }
                    });
                }
            });
        });
    });
</script>

